// Jekyll pagination functionality
document.addEventListener('DOMContentLoaded', function() {
    const perPageSelect = document.getElementById('posts-per-page');
    if (!perPageSelect) return;
    
    // Handle posts per page change
    perPageSelect.addEventListener('change', function() {
        const value = this.value;
        const currentPath = window.location.pathname;
        const baseUrl = document.querySelector('base')?.getAttribute('href') || '';
        
        // Since Jekyll generates static pages, we need to redirect to different pre-built versions
        // These will be generated by the GitHub Actions workflow
        let newPath;
        
        if (value === '5') {
            newPath = baseUrl + '/posts-5/';
        } else if (value === '25') {
            newPath = baseUrl + '/posts-25/';
        } else {
            // Default 10 per page
            newPath = baseUrl + '/posts/';
        }
        
        window.location.href = newPath;
    });
});